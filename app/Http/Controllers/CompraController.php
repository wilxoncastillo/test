    {
        $compras = Compra::all();
        $detalles = DetalleCompra::all();
        
        $data = DB::table('detalle_compras')
                 ->select('categoria_id', DB::raw('sum(precio) as total'))
                 ->groupBy('categoria_id')
                 ->get();  

        return view('welcome', [
            'compras' => $compras,
            'detalles' => $detalles,
            'data' => $data,
        ]);
    }

